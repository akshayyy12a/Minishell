#include "libft.h"

int	ft_strcpy(char *dst, char *src, int i)
{
	int	j;

	j = 0;
	if (!src)
		return (i);
	while (src[j] != '\0')
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	return (i);
}
